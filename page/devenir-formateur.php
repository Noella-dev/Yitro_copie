        // Animation au défilement et comptage progressif
        document.addEventListener('DOMContentLoaded', () => {
            // Animation des éléments
            const slideInElements = document.querySelectorAll('.welcome-content, .welcome-content h1, .welcome-content p, .btn-join, .missions, .valeurs, .nos-chiffres, .stat, .profiles-text, .profiles-image, .profiles-list li, .process-step, .tools-text, .tools-image, .tools-list li, .monetization-text, .monetization-image, .monetization-list li, .btn-monetize, .testimonials-grid, .testimonial-card, .links-section, .links-list li');
            const observer = new IntersectionObserver((entries) => {
                entries.forEach(entry => {
                    if (entry.isIntersecting) {
                        entry.target.classList.add('visible');
                        observer.unobserve(entry.target);
                    }
                });
            }, { threshold: 0.2 });

            slideInElements.forEach(element => {
                observer.observe(element);
            });

            // Comptage progressif pour les chiffres clés
            const counters = document.querySelectorAll('.stat h3');
            const animateCount = (element, target) => {
                let current = 0;
                const increment = target / 100;
                const updateCount = () => {
                    current += increment;
                    if (current < target) {
                        element.textContent = Math.ceil(current);
                        requestAnimationFrame(updateCount);
                    } else {
                        element.textContent = target.toLocaleString('fr-FR') + (target > 1000 ? '+' : '');
                    }
                };
                updateCount();
            };

            const countObserver = new IntersectionObserver((entries) => {
                entries.forEach(entry => {
                    if (entry.isIntersecting) {
                        counters.forEach(counter => {
                            const target = parseInt(counter.getAttribute('data-target'));
                            animateCount(counter, target);
                        });
                        countObserver.unobserve(entry.target);
                    }
                });
            }, { threshold: 0.5 });

            document.querySelectorAll('.nos-chiffres').forEach(section => {
                countObserver.observe(section);
            });
        });
    </script>
